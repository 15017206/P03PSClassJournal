public class Weeks {
    private String weekNo;
    private String weekGrade;


    public Weeks(String weekNo, String weekGrade) {

        this.weekNo = weekNo;
        this.weekGrade = weekGrade;
    }

    public String getWeekNo(){
        return weekNo;
    }

    public String getWeekGrade(){
        return weekGrade;
    }
}
